print("\nRe-labeling complete.")

CHUNK_SIZE = 50

print(f"Upserting {len(cluster_themes_rows)} rows to cluster_themes...")

for i in range(0, len(cluster_themes_rows), CHUNK_SIZE):
    chunk = cluster_themes_rows[i:i + CHUNK_SIZE]
    try:
        supabase.table("cluster_themes").upsert(
            chunk,
            on_conflict="cluster_id"
        ).execute()
        print(f"✅ cluster_themes: {min(i + CHUNK_SIZE, len(cluster_themes_rows))}/{len(cluster_themes_rows)}")
    except Exception as e:
        print(f"❌ Error at chunk {i}: {e}")

print("cluster_themes upsert complete.")

print(f"Upserting {len(nearest_article_updates)} nearest_article flags...")

for i in range(0, len(nearest_article_updates), CHUNK_SIZE):
    chunk = nearest_article_updates[i:i + CHUNK_SIZE]
    try:
        supabase.table("article_analysis").upsert(
            chunk,
            on_conflict="doc_id"
        ).execute()
        print(f"✅ nearest_article: {min(i + CHUNK_SIZE, len(nearest_article_updates))}/{len(nearest_article_updates)}")
    except Exception as e:
        print(f"❌ Error at chunk {i}: {e}")

print("nearest_article flags upsert complete.")