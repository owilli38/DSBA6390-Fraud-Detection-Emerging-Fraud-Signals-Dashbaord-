# Iterate through each unique cluster_id (excluding noise cluster -1)
for cluster_id in article_analysis['cluster_id'].unique():
    if cluster_id == -1:
        continue

    # Get articles belonging to the current cluster
    cluster_articles = article_analysis[article_analysis['cluster_id'] == cluster_id].copy()

    # Get the centroid for the current cluster and reshape it for cosine_similarity
    # Ensure we use the latest week's centroid or a representative one
    cluster_centroid_df = cluster_stats_df[
        (cluster_stats_df['cluster_id'] == cluster_id)
    ].sort_values(by='week', ascending=False).iloc[0]

    cluster_centroid = np.array(cluster_centroid_df['centroid']).reshape(1, -1)

    # Convert the list of embeddings for the cluster's articles into a NumPy array
    article_embeddings_in_cluster = np.array(cluster_articles['bert_embeddings'].tolist())

    # Calculate cosine similarity between each article and the cluster centroid
    similarities = cosine_similarity(article_embeddings_in_cluster, cluster_centroid)

    # Find the index of the article with the maximum similarity within this cluster
    closest_article_relative_index = np.argmax(similarities)

    # Get the doc_id of the closest article
    closest_article_doc_id = cluster_articles.iloc[closest_article_relative_index]['doc_id']

    # Update the 'nearest_article' column for the closest article in the original DataFrame
    article_analysis.loc[article_analysis['doc_id'] == closest_article_doc_id, 'nearest_article'] = True

