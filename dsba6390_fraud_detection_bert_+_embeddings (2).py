def classify_stage(age_days, growth, acceleration, drift):

    if growth < -0.2:
        return "declining" # decay detection

    if age_days <= 30:
        if growth > 0.3 or drift > 0.2:
            return "emerging"

    elif 30 < age_days <= 90:
        if growth > 0.1:
            return "trending"

    elif age_days >= 180:
        if acceleration < 0.05:
            return "established"

    return "stable" # 90-180 days cluster classifier

