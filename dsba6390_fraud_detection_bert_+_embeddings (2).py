# Function to safely parse an embedding value into a list of floats
def parse_embedding_to_list(embed_value):
    if isinstance(embed_value, str):
        try:
            # Use ast.literal_eval to safely convert string to Python literal (list, tuple, etc.)
            parsed = ast.literal_eval(embed_value)
            if isinstance(parsed, (list, tuple)):
                return [float(x) for x in parsed] # Ensure elements are floats
        except (ValueError, SyntaxError):
            return None # Not a valid string representation of a list
    elif isinstance(embed_value, np.ndarray):
        return embed_value.tolist()
    elif isinstance(embed_value, list):
        # Ensure it's a list of floats
        return [float(x) for x in embed_value]
    return None # For any other unexpected types or None values

